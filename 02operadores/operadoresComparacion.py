a = 4
b = 2

# preguntar si dos valores son iguales
resultado = (a==b)
print(f'Resulatdo  ==: {resultado}')

# pregunatr si dos valores son distintos
resultado = (a != b)
print(f'resultadi != : {resultado}')

# operador mayor que
resultado = a > b
print(f'Resultado >: {resultado}')

# operador mayor o igual que
resultado = a >= b
print(f'Resultado >=: {resultado}')

# operador menor que
resultado = a < b
print(f'Resultado <: {resultado}')

# operador menor o igual que
resultado = a <= b
print(f'Resultado <=: {resultado}')