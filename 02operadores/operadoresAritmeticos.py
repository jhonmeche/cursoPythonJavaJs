#suma
operandoA = 3
operandoB = 2

suma = operandoA + operandoB
#print('Resultado de la suma:', suma)
print(f'Resultado de la suma: {suma}')

# resta
resta = operandoA - operandoB
print(f'Resultado de la resta: {resta}')

#multiplidcacion 
multiplicacicion = operandoA * operandoB
print(f'Resultado de la multiplicacion: {multiplicacicion}')

# division 
division = operandoA / operandoB
print(f'Resultado de la division: {division}')

# division sin punto flotante
division = operandoA // operandoB
print(f'Resultado de la division (int): {division}')

# modulo o residuo
modulo = operandoA % operandoB
print(f'Resultado del residuo de la division: {modulo}')

# potencia 
potencia = operandoA ** operandoB
print(f'Resultado de la potencia: {potencia}')

